SELECT EMPNO,ENAME,DEPTNO,
CASE WHEN DEPTNO = 10 THEN '인사부'
WHEN DEPTNO = 20 THEN '영업부'
WHEN DEPTNO = 30 THEN '개발부'
ELSE '생산부'
END AS '부서명'
 FROM emp; 

