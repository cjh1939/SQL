SELECT EMPNO,ENAME,HIREDATE,SAL, E.DEPTNO FROM emp E
INNER JOIN dept D
ON D.DEPTNO = E.DEPTNO
WHERE DNAME != '인사부' AND SAL >= 500 ORDER BY EMPNO DESC ,ENAME ASC;

