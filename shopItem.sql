CREATE TABLE ITEM_INFO(
	ITEM_NUM INT PRIMARY KEY AUTO_INCREMENT
	,ITEM_NAME VARCHAR(20) NOT NULL UNIQUE
	,ITEM_PRICE INT NOT NULL 
	,SELLER VARCHAR(10)
	,ITEM_INTRO VARCHAR(20)
	,REG_DATE DATETIME DEFAULT SYSDATE() 

);
